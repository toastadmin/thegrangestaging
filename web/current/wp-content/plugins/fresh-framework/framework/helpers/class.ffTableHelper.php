<?php

/**
 * Class ffTableHelper
 * For easy table creating
 */
class ffTableHelper extends ffBasicObject {
/**********************************************************************************************************************/
/* OBJECTS
/**********************************************************************************************************************/

/**********************************************************************************************************************/
/* PRIVATE VARIABLES
/**********************************************************************************************************************/
    private $_data = array();

    private $_row = null;
/**********************************************************************************************************************/
/* CONSTRUCT
/**********************************************************************************************************************/

/**********************************************************************************************************************/
/* PUBLIC FUNCTIONS
/**********************************************************************************************************************/
    public function startNewRow() {
        $newRow = array();
        $this->_row = &$newRow;
        $this->_data[] = &$newRow;
    }

    public function addColumn( $content ) {
        $this->_row[] = $content;
    }

    public function renderTable() {
        echo '<table style="border:1px solid black;">';
            foreach( $this->_data as $oneRow ) {
                echo '<tr style="border:1px solid black;">';
                    foreach( $oneRow as $oneCell ) {
                        echo '<td style="border:1px solid black;">';
                            echo $oneCell;
                        echo '</td>';
                    }
                echo '</tr>';
            }
        echo '</table>';
    }
/**********************************************************************************************************************/
/* PUBLIC PROPERTIES
/**********************************************************************************************************************/

/**********************************************************************************************************************/
/* PRIVATE FUNCTIONS
/**********************************************************************************************************************/

/**********************************************************************************************************************/
/* PRIVATE GETTERS & SETTERS
/**********************************************************************************************************************/
}